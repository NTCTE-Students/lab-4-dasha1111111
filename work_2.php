<?php
function isPalindrome($str) {
    // Убираем все пробелы и приводим строку к нижнему регистру для корректного сравнения
    $str = strtolower(str_replace(' ', '', $str));

    // Сравниваем строку с её перевёрнутым вариантом
    if ($str === strrev($str)) {
        return true; // Это палиндром
    } else {
        return false; // Это не палиндром
    }
}

// Вызов функции
$word = "А роза упала на лапу Азора";  // Пример с пробелами
if (isPalindrome($word)) {
    echo "Строка является палиндромом.";
} else {
    echo "Строка не является палиндромом.";
}
?>
